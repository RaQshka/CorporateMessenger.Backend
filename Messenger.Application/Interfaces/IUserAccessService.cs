namespace Messenger.Application.Interfaces;

public interface IUserAccessService
{
    /// <summary>
    /// Проверяет, имеет ли пользователь право выполнять действие в чате.
    /// Например, отправлять сообщение, если его роль входит в разрешённые в чате.
    /// </summary>
    Task<bool> CanSendMessageAsync(Guid chatId, Guid userId);

    /// <summary>
    /// Проверяет, имеет ли пользователь право быть добавленным в чат.
    /// Можно расширить логику для добавления/удаления.
    /// </summary>
    Task<bool> CanJoinChatAsync(Guid chatId, Guid userId);

    /// <summary>
    /// Проверяет, имеет ли пользователь право назначать администраторов в чате.
    /// Например, только создатель или текущие администраторы.
    /// </summary>
    Task<bool> CanAssignAdminAsync(Guid chatId, Guid userId);
}
